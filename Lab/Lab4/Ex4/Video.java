package Ex4;

class Video {
    String title;
    String uploader;
    double lengthMinutes;
    String type;

    public Video(String title, String uploader, double lengthMinutes, String type) {
        this.title = title;
        this.uploader = uploader;
        this.lengthMinutes = lengthMinutes;
        this.type = type;
    }
}