public class Main {
        public static void main(String[] args) {
        System.out.printf("%-15s %-15s %n", "\\n","Line break.");
        System.out.printf("%-15s %-15s %n", "\\t","Tabulation.");
        System.out.printf("%-15s %-15s %n", "\\'","Single Quote.");
        System.out.printf("%-15s %-15s %n", "\\\"","Double Quote.");
        System.out.printf("%-15s %-15s %n", "\\\\","\\ Sign.");
        System.out.printf("%-15s %-15s %n", "\\\\\\\\","\\\\ Sign.");
        System.out.printf("%-15s %-15s %n", "//","Line Comment.");
        System.out.printf("%-15s %-15s %n", "/* ... */","Block Comment.");
        System.out.printf("%-15s %n", "\"\"\"");
        System.out.printf("%-15s %-15s %n", "","Text block.");
        System.out.printf("%-15s %n", "\"\"\"");
    }
}
